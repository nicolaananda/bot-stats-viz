// Simple favicon generator
const fs = require('fs');

// Create a simple 16x16 favicon with 'GH' text
// This is a minimal ICO file structure
const icoData = Buffer.from([
  // ICO header
  0x00, 0x00, // Reserved
  0x01, 0x00, // Type (1 = ICO)
  0x01, 0x00, // Number of images
  
  // Image directory entry
  0x10, 0x00, 0x00, 0x00, // Width/Height (16x16)
  0x00, 0x00, // Color planes
  0x01, 0x00, // Bits per pixel
  0x20, 0x00, 0x00, 0x00, // Size of image data
  0x16, 0x00, 0x00, 0x00, // Offset to image data
  
  // BMP data (simplified)
  // BMP header
  0x28, 0x00, 0x00, 0x00, // Header size
  0x10, 0x00, 0x00, 0x00, // Width
  0x10, 0x00, 0x00, 0x00, // Height
  0x01, 0x00, // Planes
  0x01, 0x00, // Bits per pixel
  0x00, 0x00, 0x00, 0x00, // Compression
  0x00, 0x00, 0x00, 0x00, // Image size
  0x00, 0x00, 0x00, 0x00, // X pixels per meter
  0x00, 0x00, 0x00, 0x00, // Y pixels per meter
  0x02, 0x00, 0x00, 0x00, // Colors used
  0x00, 0x00, 0x00, 0x00, // Important colors
  
  // Color palette
  0x00, 0x00, 0x00, 0x00, // Black
  0xFF, 0xFF, 0xFF, 0x00, // White
  
  // Image data (16x16 = 256 bits = 32 bytes)
  // Each row is 2 bytes (16 pixels = 16 bits)
  // Pattern for 'GH' text
  0x00, 0x00, // Row 1
  0x00, 0x00, // Row 2
  0x00, 0x00, // Row 3
  0x00, 0x00, // Row 4
  0x00, 0x00, // Row 5
  0x00, 0x00, // Row 6
  0x00, 0x00, // Row 7
  0x00, 0x00, // Row 8
  0x00, 0x00, // Row 9
  0x00, 0x00, // Row 10
  0x00, 0x00, // Row 11
  0x00, 0x00, // Row 12
  0x00, 0x00, // Row 13
  0x00, 0x00, // Row 14
  0x00, 0x00, // Row 15
  0x00, 0x00  // Row 16
]);

fs.writeFileSync('public/favicon.ico', icoData);
console.log('Favicon created successfully');
